public class FuncionToUpperCase {
    public static void main(String[] args) {

        String t = "taller de Programacion i"; //Convertie de minusculas a Mayusculas
        System.out.println(t);
        t = t.toUpperCase();
        System.out.println("Ahora en mayuscula: "+t);

    }
}
